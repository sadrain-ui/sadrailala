function buildCells(): UniversalIngressCell[] {
  const out: UniversalIngressCell[] = []
  let i = 0
  for (const f of FEATURED_ARCH) {
    if (i >= UNIVERSAL_INGRESS_CELL_COUNT) break
    out.push({
      id: `uig-${i}`,
      archLabel: f.label,
      glyph: f.glyph,
    })
    i += 1
  }
  while (out.length < UNIVERSAL_INGRESS_CELL_COUNT) {
    const poolIdx = out.length % ARCHITECTURE_POOL.length
    const generation = Math.floor(out.length / ARCHITECTURE_POOL.length)
    const base = ARCHITECTURE_POOL[poolIdx] ?? 'ingress.unknown'
    const archLabel =
      generation === 0 ? base : `${base} · layer-${generation}`
    out.push({
      id: `uig-${out.length}`,
      archLabel,
    })
  }
  return out
}

export const UNIVERSAL_INGRESS_CELLS: readonly UniversalIngressCell[] = buildCells()
